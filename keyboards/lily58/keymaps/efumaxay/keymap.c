bool oled_task_user(void) {
    if (!is_keyboard_master()) {
        return false;
    }

    if (is_caps_word_on()) {
        capslock_logo();
        return false;
    }

    switch (get_highest_layer(layer_state)) {
        case 0:
            layer1_logo();
            break;
        case _LOWER:
            layer2_logo();
            break;
        case _RAISE:
            layer3_logo();
            break;
        case _ADJUST:
            layer4_logo();
            break;
    }

    return false;
}
