void process_combo_event(uint16_t combo_index, bool pressed) {
    switch(combo_index) {
        case FS_LALT:
            if (pressed) {
                combo_fs_active = true;
                register_code(KC_LALT);
            } else {
                combo_fs_active = false;
                unregister_code(KC_LALT);
                unregister_code(KC_LSFT);
            }
            break;
        case FD_LCTL:
            if (pressed) {
                combo_fd_active = true;
                register_code(KC_LCTL);
            } else {
                combo_fd_active = false;
                unregister_code(KC_LCTL);
                unregister_code(KC_LSFT);
            }
            break;
        case JK_LCTL:
            if (pressed) {
                combo_jk_active = true;
                register_code(KC_LCTL);
            } else {
                combo_jk_active = false;
                unregister_code(KC_LCTL);
                unregister_code(KC_LSFT);
            }
            break;
        case JL_LALT:
            if (pressed) {
                combo_jl_active = true;
                register_code(KC_LALT);
            } else {
                combo_jl_active = false;
                unregister_code(KC_LALT);
                unregister_code(KC_LSFT);
            }
            break;
        default:
            combo_fs_active = false;
            combo_fd_active = false;
            combo_jk_active = false;
            combo_jl_active = false;
            break;
    }
}

